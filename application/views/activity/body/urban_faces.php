<h3>Leven in de stad</h3>
<p class="intro">Tijd om een beetje leven in de brouwerij te brengen. Ontwerp een set ogen, monden en tanden, print deze op papier &ndash; of nog beter, op stickerpapier &ndash; en ga de stad in.</p>
<p>Je bent op zoek naar voorwerpen die makkelijk om te toveren zijn tot een gezicht. Neem bijvoorbeeld een putdeksel en plak er een bril en een mond op of plak een set ogen of tanden op een prullenbak.</p>
