<h3>Creatief met karton</h3>
<p class="intro">Met karton kun je zelf je eigen boombox in elkaar zetten. Zorg dat je begint met een flinke doos, kleiner kan altijd nog, en tik ergens een oude radio of wekker radio op de kop die je erin kunt bouwen. </p>
<p><img src="<?=site_url('images/activities/kartonnen-boombox.jpg');?>" alt=""></p>
<p>Wil je het echt spectaculair aanpakken? Probeer dan om de kartonnen knoppen aan te laten sluiten op de knoppen van je radio, zodat je je kartonnen boombox echt kunt gebruiken. Wees niet bang om de radio uit elkaar te halen, zolang je de bedrading heel laat blijft hij het gewoon doen. Pas wel goed op dat de stekker niet in het stopcontact zit.</p>
<p><img src="<?=site_url('images/activities_body/radio.jpg');?>" alt=""></p>
<p>Onderdelen die bij je boombox zeker niet mogen ontberen:</p>
