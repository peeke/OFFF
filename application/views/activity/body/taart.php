<h3>Taart bakken</h3>
<p class="intro">Heb je zin in iets lekkers, maar is er niks in huis dat je aanstaat? Ga zelf aan de slag!</p>
<p>Bak een lekkere taart! Hieronder vind je het recept voor een appeltaart, maar je kunt natuurlijk ook een <a href="http://www.okokorecepten.nl/zoeken/index.asp?go=1&tt=kwarktaart" target="_blank">kwarttaart</a> of een <a href="http://www.okokorecepten.nl/zoeken/index.asp?go=1&tt=cake" target="_blank">cake</a> bakken. Of in de supermarkt een recept doos halen, lekker makkelijk!</p>
<p>
	Het volgende recept komt van <a href="http://www.okokorecepten.nl/" target="_blank">Okoko recepten</a>.
</p>
<h4>Ingredi&euml;nten</h4>
<p>
	<ul>
		<li class="ingredient"><span class="name">120 g boter</span></li>
		<li class="ingredient"><span class="name">120 g bloem</span></li>
		<li class="ingredient"><span class="name">2 eieren</span></li>
		<li class="ingredient"><span class="name">100 g rietsuiker</span></li>
		<li class="ingredient"><span class="name">1 eetlepel bakpoeder</span></li>
		<li class="ingredient"><span class="name">1 kg appels</span></li>
		<li class="ingredient"><span class="name">sppelstroop</span></li>
	</ul>
</p>
<h4>Bereiden</h4>
<p>Verwarm de oven voor op 180 Â°C.</p>
<p>Beboter een cakevorm en bestuif met bloem.</p>
<p>Smelt de boter en laat afkoelen tot kamertemperatuur.</p>
<p>Splits de eieren. Klop de eierdooiers en de suiker tot een dikke, luchtige massa. Zeef de bloem erbij. Doe de boter en het bakpoeder bij het beslag en klop glad.</p>
<p>Schil de appels, verwijder het klokhuis en snijd de helft van de appels in kleine blokjes en de rest in fijne schijfjes.</p>
<p>Doe de appelblokjes in de cakevorm. Giet het beslag erover. Schik de appelschijfjes er mooi op. Bak de taart 45 minuten in de oven.</p>
<p>Warm de appelstroop lichtjes op. Haal de taart uit de oven en bestrijk met de stroop. Laat afkoelen op een taartrooster.</p>

