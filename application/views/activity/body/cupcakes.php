<h4>Bereidingswijze</h4>

<p>Verwarm de oven voor op 170 graden en zorg ervoor dat het rooster in het midden van de oven geplaatst is. Zet een cupcake bakblik met daarin papieren bakvormpjes klaar. </p>

<p>Snij de boter in blokjes. Het is belangrijk dat de boter goed op kamertemperatuur is gekomen. Doe de boter in een kom en voeg de suiker  beetje bij beetje toe. Mix het mengsel steeds tussendoor met de mixer op middelhoge snelheid, totdat er een lichte en luchtige massa ontstaat.</p>

<p>Voeg nu 1 voor 1 de eieren toe. Mix het beslag goed door met de mixer op middelhoge snelheid als je een ei hebt toegevoegd.</p>

<p>Voeg nu beetje bij beetje het zelfrijzend bakmeel toe. Mix telkens de massa met de mixer op middelhoge snelheid als je een hoeveelheid meel hebt toegevoegd.</p>

<p>Als laatste voeg je de melk toe en mix je nog eens 1 minuut alles goed door elkaar met de mixer op middelhoge snelheid, tot een luchtig en glad beslag. Pak je cupcake bakblik erbij met daarin de papieren bakvormpjes en vul de papieren bakvormpjes tot 2/3. </p>

<h4>Bakken</h4>
<p>Bak de cupcakes 20-25 minuten in de oven op 170 graden. Open de ovendeur tussentijds niet, anders zakken de cupcakes misschien in. Na 20 minuten open je de ovendeur en prik je met een houten prikker in een cupcake om te controleren of de cupcakes gaar zijn. Wanneer er geen nat beslag aan de houten prikker blijft kleven kunnen de cupcakes uit de oven gehaald worden. </p>

<p>Eenmaal uit de oven laat je de cupcakes even een minuut in het bakblik zitten. Zet het bakblik bij voorkeur even op een rooster. Na een minuut haal je de cupcakes voorzichtig uit het bakblik. </p>

