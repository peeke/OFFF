<h3>Met mos?</h3>
<p class="intro">
	Graffiti met mos, heb je daar ooit van gehoord?
</p>
<p>Allereerst: credits voor <a href="http://www.annagarforth.co.uk/work/grow.html" target="_blank">Anna Garforth</a>, maker van de cover afbeelding.</p>
<p>Vervolgens: hoe doe je dit zelf? </p>
<p>Mix anderhalve kop karnemelk met een paar handen mos en een teelepel suiker. Voor het beste resultaat stop je alles in de blender, maar als je het mos fijn snijdt en het met een mixer mixt moet het ook lukken. Als het goed is krijg je een substantie die dik genoeg is om mee te schilderen. </p>
<p>
	<img src="<?=site_url('images/activities_body/mos.jpg')?>">
</p>
<p>Vervolgens schilder je met het mengsel op een poreuze muur, bijvoorbeeld een van baksteen of beton. Het helpt als je het mengsel dagelijks/wekelijks met water kunt bemisten tot het mos begint te groeien. Lukt het niet? Breng dan nog wat extra van het mengsel aan.</p>
<p>
	<img src="<?=site_url('images/activities_body/mos_graffiti.jpg')?>">
</p>
<p>Om een strakken vorm te maken kun je vantevoren een vorm aftapen of kun je een stencil maken, zoals in <a href="http://www.cutoutandkeep.net/projects/stencilling_tutorial" target="_blank">deze tutorial</a>.</p>
<p>Deel hieronder je foto's en strijk maar liefst 45 punten op voor deze actie!</p>