<h3>De vastgelijmde munt</h3>
<p class="intro">Je ziet een euro liggen, wat een geluk! Wanneer je bukt om de euro op te rapen krijg je hem echter met geen mogelijkheid van de grond af. Wat is hier aan de hand?</p>
<p>De vastgelijmde munt is een klassieke grap. Met secondenlijm of 2-componenten lijm je de munt ergens vast. Daarna is het slechts een kwestie van wachten. Als extra kun je op de onderkant van de munt nog een boodschap achterlaten voor degenen die het tegen alle kansen in toch mocht lukken de munt op te rapen!</p>
